/**
 * @description https://wiki.libsdl.org/SDL3/SDL_FlipMode
 */
export enum SDL_FlipMode {
  /**< Do not flip */
  SDL_FLIP_NONE,
  /**< flip horizontally */
  SDL_FLIP_HORIZONTAL,
  /**< flip vertically */
  SDL_FLIP_VERTICAL,
  /**< flip horizontally and vertically (not a diagonal flip) */
  SDL_FLIP_HORIZONTAL_AND_VERTICAL,
}
